# Teaching sign — the "tell" beat. Calibrating kid-friendly tone + length:
# short, one idea, grounded in something they already know.
color = white
box(width = 1, height = 4, depth = 1, color = white) # a little signpost

let lesson_sign = say("- What's a loop?",
  """
  # What's a **loop**?

  You already know loops!

  - "Do 10 jumping jacks."
  - "Stir 20 times."

  A loop means: **do something again and again** a set number of times —
  without writing it out every time.

  See that spiral staircase? A loop built it: *forward, turn, step up* —
  over and over. Let's make one.
  """,
  width = 4.0)
